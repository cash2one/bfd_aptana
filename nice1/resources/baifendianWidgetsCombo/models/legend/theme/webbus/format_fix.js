;(function($){
	var typeFix = {
		'bus_':function(v){
			switch(v){
				default:
					return '未知';
			};
		},
		'bus_cpc':function(v){
			return 'CPC'
		},
		'bus_cps':function(v){
			return 'CPS'
		},
		'bus_href_fin_audit':function(v){
			return '<a href="./audit.html">'+v+'</a>'
		}
	}
	$.extend(window.BfdWidget.format,typeFix);
})(jQuery);